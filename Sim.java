import java.awt.*;
import java.awt.event.*;
import javax.swing.*;

public class Sim {
    MainWindow mainWindow;
    SplashWindow splashWindow;

    public static void main(String[] args){
        new Sim();
    }

    public Sim(){
        splashWindow = new SplashWindow();
        try {Thread.sleep(DFN.CLOGO_TIME);} catch (InterruptedException e) {}
        splashWindow.changeLogo();
        try {Thread.sleep(DFN.ELOGO_TIME);} catch (InterruptedException e) {}
        splashWindow.killLogo();
        mainWindow = new MainWindow();
    }

}